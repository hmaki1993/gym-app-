// Priority: existing 3D GIFs > ExerciseDB > fitnessprogramer
export const PREMIUM_GIFS: Record<string, string> = { ...FITNESS_PROGRAMER_GIFS, ...EXERCISEDB_GIFS, ...HIGH_QUALITY_GIFS };

export function getExerciseGifUrl(name: string): string | null {
  const cleanName = name.trim().toLowerCase();

  // Resolve alias first to make sure variations like "incline bench" or "benchpress" map correctly
  const resolvedName = EXTRA_ALIASES[cleanName] || cleanName;

  // 1. First check if it matches a pre-cached clean, smooth, official ExerciseDB press GIF.
  // This provides INSTANT load speed (no 1.4MB JSON network fetch) and ZERO ghosts.
  if (CLEAN_EXERCISEDB_PRESS_GIFS[cleanName]) {
    return CLEAN_EXERCISEDB_PRESS_GIFS[cleanName];
  }
  if (CLEAN_EXERCISEDB_PRESS_GIFS[resolvedName]) {
    return CLEAN_EXERCISEDB_PRESS_GIFS[resolvedName];
  }

  // 2. Check Franco-Arabic spelling (e.g., "bensh pres" → "bench press")
  const francoKey = Object.keys(FRANCO_ALIASES).find(k => k === cleanName || cleanName.includes(k) || k.includes(cleanName));
  const aliasName = (francoKey ? FRANCO_ALIASES[francoKey] : null) || EXTRA_ALIASES[cleanName] || cleanName;

  // 3. Fall through to PREMIUM_GIFS
  if (PREMIUM_GIFS[cleanName]) return PREMIUM_GIFS[cleanName];
  if (aliasName !== cleanName && PREMIUM_GIFS[aliasName]) return PREMIUM_GIFS[aliasName];

  const foundKey = Object.keys(PREMIUM_GIFS).find(k =>
    cleanName.includes(k) || k.includes(cleanName)
  );
  if (foundKey) return PREMIUM_GIFS[foundKey];

  const aliasKey = Object.keys(EXTRA_ALIASES).find(k =>
    cleanName.includes(k) || k.includes(cleanName)
  );
  if (aliasKey && PREMIUM_GIFS[EXTRA_ALIASES[aliasKey]]) {
    return PREMIUM_GIFS[EXTRA_ALIASES[aliasKey]];
  }

  return null;
}
